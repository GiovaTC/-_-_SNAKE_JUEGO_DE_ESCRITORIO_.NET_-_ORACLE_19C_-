--? Tabla de puntajes
CREATE TABLE GAME_SCORES_X (
    ID          NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PLAYER_NAME VARCHAR2(50),
    SCORE       NUMBER,
    PLAY_DATE   DATE DEFAULT SYSDATE,
    CONSTRAINT PK_GAME_SCORES PRIMARY KEY (ID)
);

COMMIT;
--? Stored Procedure (SP)
CREATE OR REPLACE PROCEDURE SP_INSERT_SCORE (
    P_PLAYER_NAME IN VARCHAR2,
    P_SCORE       IN NUMBER
)
AS
BEGIN
    INSERT INTO GAME_SCORES_X (PLAYER_NAME, SCORE)
    VALUES (P_PLAYER_NAME, P_SCORE);

    COMMIT;
END;
/

COMMIT;